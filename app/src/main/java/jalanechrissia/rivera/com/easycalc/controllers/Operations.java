public class Operations {

    public double add(double n1, double n2){
        return n1+n2;
    }

    public double sub(double n1, double n2){
        return n1-n2;
    }

    public double mult(double n1, double n2){
        return n1*n2;
    }

    public double div(double n1, double n2){
        return n1/n2;
    }
}
